package aula2403;

public class Conta {
	//atributos
	double Saldo;
	String nrAgencia;
	String titular;
	String nrConta;
	int codBanco;
	
	//construtores
	public Conta(double saldo, String nrAgencia, String titular, String nrConta, int codBanco) {
		super();
		Saldo = saldo;
		this.nrAgencia = nrAgencia;
		this.titular = titular;
		this.nrConta = nrConta;
		this.codBanco = codBanco;
	}
	
	//getters e setters
	
	
	
}
